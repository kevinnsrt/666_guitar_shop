    private void getData(){
    DefaultTableModel model = (DefaultTableModel) tbl_pesanan.getModel();
    model.setRowCount(0); 
    
    try {
        String sql = "SELECT * FROM history_pesanan";
        PreparedStatement st = conn.prepareStatement(sql);
        ResultSet rs = st.executeQuery();

        while (rs.next()) {
            int id_pesanan = rs.getInt("id_history_pesanan");
            int id = rs.getInt("id_barang");
            String nama_pesanan = rs.getString("pesanan");
            String nama_user = rs.getString("nama_user");

           
            Object[] rowData = {id_pesanan,id,nama_pesanan, nama_user};
            model.addRow(rowData);
        }

        rs.close();
        st.close();
    } catch (Exception e) {
        Logger.getLogger(HistoryPesanan.class.getName()).log(Level.SEVERE, null, e);
    }
    }
